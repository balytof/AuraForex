function buildI18nMap(lang) {
    if (lang === 'pt') return null;
    const dict = dashboardTranslations[lang];
    if (!dict) return null;
    
    let map = [];
    for (let key in dict) {
        map.push({
            ptLower: key.toLowerCase(),
            target: dict[key],
            original: key
        });
    }
    map.sort((a,b) => b.ptLower.length - a.ptLower.length);
    return map;
}

window.applyDashboardI18n = function(forceLang) {
    const lang = forceLang || localStorage.getItem('aura_lang');
    if (!lang || lang === 'pt') return;
    
    const map = buildI18nMap(lang);
    if (!map) return;

    function walkNodes(node) {
        if (node.nodeType === 3) {
            let text = node.nodeValue;
            if (!text || !text.trim()) return;
            
            let changed = false;
            let lowerText = text.toLowerCase();
            
            for (let m of map) {
                if (lowerText.includes(m.ptLower)) {
                    const regex = new RegExp(m.original.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi');
                    text = text.replace(regex, m.target);
                    changed = true;
                    lowerText = text.toLowerCase();
                }
            }
            if (changed) node.nodeValue = text;
        } else if (node.nodeType === 1) {
            if (['SCRIPT', 'STYLE', 'INPUT', 'TEXTAREA'].includes(node.nodeName)) return;
            for (let child of Array.from(node.childNodes)) {
                walkNodes(child);
            }
            if (node.placeholder) {
                let text = node.placeholder;
                let changed = false;
                let lowerText = text.toLowerCase();
                for (let m of map) {
                    if (lowerText.includes(m.ptLower)) {
                        const regex = new RegExp(m.original.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi');
                        text = text.replace(regex, m.target);
                        changed = true;
                        lowerText = text.toLowerCase();
                    }
                }
                if (changed) node.placeholder = text;
            }
        }
    }

    walkNodes(document.body);
    
    // Set up MutationObserver only once
    if (!window.i18nObserver) {
        window.i18nObserver = new MutationObserver((mutations) => {
            mutations.forEach((m) => {
                m.addedNodes.forEach(node => {
                    if (node.nodeType === 1 || node.nodeType === 3) walkNodes(node);
                });
            });
        });
        window.i18nObserver.observe(document.body, { childList: true, subtree: true });
    }
}

window.forceDashboardLang = function(lang) {
    localStorage.setItem('aura_lang', lang);
    location.reload(); 
};

document.addEventListener('DOMContentLoaded', () => window.applyDashboardI18n());
if (document.readyState === 'complete' || document.readyState === 'interactive') {
    window.applyDashboardI18n();
}
